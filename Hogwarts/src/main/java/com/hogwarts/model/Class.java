package com.hogwarts.model;

public class Class {
	int classId;
	String className;

	public int getClassId() {
		return classId;
	}

	public void setClassId(int class_id) {
		this.classId = class_id;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String class_name) {
		this.className = class_name;
	}
}
